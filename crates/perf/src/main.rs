fn warmup() {
    use cynos_index::BTreeIndex;
    let mut tree: BTreeIndex<i64> = BTreeIndex::new(64, false);
    for i in 0..1000i64 {
        tree.add(i, i as u64).unwrap();
    }
    for i in 0..1000i64 {
        let _ = tree.get(&i);
    }
}
